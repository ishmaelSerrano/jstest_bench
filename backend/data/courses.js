// takes a string called getCategory and an array called getIds
// 1 returns all the courses in the getIds column that have the category in the getCategory
// 2 if only given a category returns all courses with that category
// 3 if only given a getIds array returns all those courses
// 4 returns empty array if the courses in getIds don't contain the getCategory
// 5 returns all courses when no arguments are given
exports.findCourses = ({category, ids}) => {
    if(category){
        return category === '' ? this.COURSES : this.COURSES.filter(course => course.category === category);
    } else if(ids){
        return this.COURSES.filter(course => ids.includes(course.id));
    } else {
        return this.courses;
    }

}

exports.findCourseById = (id) => {
    return this.COURSES.find(course => course.id === parseInt(id));
}
